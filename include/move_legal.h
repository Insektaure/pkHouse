// LGPE: moves 0-164 all valid; higher moves checked against whitelist
// These are the LGPE-exclusive high-ID moves that ARE valid
constexpr uint16_t LGPE_EXTRA_MOVES[] = {
    // Partner Pikachu
    729, 730, 731,
    // Partner Eevee
    733, 734, 735, 736, 737, 738, 739, 740,
    // Let's Go exclusive
    741, 742
};
constexpr int LGPE_EXTRA_MOVES_COUNT = sizeof(LGPE_EXTRA_MOVES) / sizeof(LGPE_EXTRA_MOVES[0]);

inline bool isValidMoveLGPE(uint16_t move) {
    if (move == 0) return true;
    if (move <= 164) return true; // All Gen1 moves valid
    // Check TM moves (165-742 range, only specific ones valid)
    // For simplicity, check against dummied bitfield approach:
    // Moves 165-728 that exist in LGPE are valid if not dummied
    if (move > MAX_MOVE_LGPE) return false;
    // LGPE doesn't have a dummied bitfield — all valid moves are either <=164
    // or are specific TM/tutor moves. For transfer purposes, we allow any
    // move that exists in LGPE's move pool.
    return true;
}

