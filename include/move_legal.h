// Check if a move is dummied (unusable) in a given bitfield
inline bool isDummied(const uint8_t* bitSet, int bitSetLen, uint16_t move) {
    int offset = move >> 3;
    if (offset >= bitSetLen) return false;
    int bit = move & 7;
    return (bitSet[offset] & (1 << bit)) != 0;
}

