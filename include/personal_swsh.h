// Get the table entry index for a species + form combination
inline int getEntryIndex(uint16_t natdex, uint8_t form = 0) {
    if (natdex >= NUM_ENTRIES) return 0;
    if (form == 0) return natdex;
    uint16_t fsi = FORM_STATS_INDEX[natdex];
    uint8_t fc = FORM_COUNT[natdex];
    if (fsi == 0 || form >= fc) return natdex;
    int idx = fsi + form - 1;
    return (idx < NUM_ENTRIES) ? idx : natdex;
}

inline uint16_t getAbility(uint16_t natdex, uint8_t form, int abilityIndex) {
    int idx = getEntryIndex(natdex, form);
    const auto& e = ABILITIES[idx];
    switch (abilityIndex) {
        case 0: return e.ab0;
        case 1: return e.ab1;
        case 2: return e.abH;
        default: return e.ab0;
    }
}

